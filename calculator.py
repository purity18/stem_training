#Complex calculator
num_1 = int(input("enter_1st_num"))
op = input("op")
num_2 = int(input("enter_2nd_num"))

if op == "*" :
    print(num_1*num_2)
elif op == "+" :
    print(num_1+num_2)  
elif op == "-" :
    print(num_1- num_2) 
elif op == "/" :
    print(num_1 / num_2) 
else : 
    print('input_operator') 
    




